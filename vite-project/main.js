const cellSize = 40;
const world = engine.world;

function drawMap(){
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[i].length; j++) {
            if (map[i][j] === 1) {
                const x = j * cellSize + cellSize / 2;
                const y = i * cellSize + cellSize / 2;
                const block = Bodies.rectangle(x, y, cellSize, cellSize, {isStatic: true});
                World.add(world, block);
            }
            if(map[i][j] === 2){
                const x = j * cellSize + cellSize / 2;
                const y = i * cellSize + cellSize / 2;
                const block = Bodies.rectangle(x, y, cellSize, cellSize,{
                    render:{
                        fillStyle:"#3295a8"
                    },
                    isStatic:true
                });
                World.add(world, block);
            }
        }
    }
}
