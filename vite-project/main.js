const cellSize = 30;

function drawMap() {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[i].length; j++) {
            const x = j * cellSize + cellSize / 2;
            const y = i * cellSize + cellSize / 2;
            if (map[i][j] === 1) {
                const wall = Bodies.rectangle(x, y, cellSize, cellSize, {
                    isStatic: true,
                    label: 'wall',
                    render: {
                        fillStyle: "#2d4e75"
                    },
                    friction:0,
                });
                World.add(world, wall);
            }
            if(map[i][j] === 2){
                let coins = new Coins(x, y);
                World.add(world, coins.coins)
                tabCoins.push(coins.coins)
            }
            if(map[i][j] === 3){
                let ghost = new Ghost(x, y)
                World.add(world, ghost.ghostMob)
            }
        }
    }
}

